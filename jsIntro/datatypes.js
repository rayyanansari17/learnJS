// Data types represent the different kinds of values we can use in JavaScript.

//There are altogether 8 basic data types in JavaScript.


// string in single quotes
let fruit = 'apple';
console.log(fruit)

// string in double quotes
let country = "USA";
console.log(country);

// string in backticks
let result = `fail`;
console.log(result);




// integer value
let integer_number = -6;
console.log(integer_number);

// floating-point value
let float_number = 8.6;
console.log(float_number);




// BigInt value
let value1 = 900719925124740998n;

// add two big integers
let result1 = value1 + 1n;
console.log(result1);  

let value2 = 940807498734873048n;



let data = true;
console.log(data);  // true

let value = false;
console.log(value);  // false


let name = undefined;
console.log(name);  // undefined



let number = null;
console.log(number);  // null


